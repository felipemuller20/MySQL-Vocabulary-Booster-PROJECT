SELECT JOB_TITLE AS 'Cargo',
	((MAX_SALARY + MIN_SALARY) DIV 2) AS 'Média Salarial',
    CASE 
		WHEN ROUND((MAX_SALARY + MIN_SALARY) DIV 2, 2) > 2000 AND ROUND((MAX_SALARY + MIN_SALARY) DIV 2, 2) < 5800 THEN 'Júnior'
        WHEN ROUND((MAX_SALARY + MIN_SALARY) DIV 2, 2) > 5801 AND ROUND((MAX_SALARY + MIN_SALARY) DIV 2, 2) < 7500 THEN 'Pleno'
        WHEN ROUND((MAX_SALARY + MIN_SALARY) DIV 2, 2) > 7501 AND ROUND((MAX_SALARY + MIN_SALARY) DIV 2, 2) < 10500 THEN 'Senior'
        WHEN ROUND((MAX_SALARY + MIN_SALARY) DIV 2, 2) > 10500 THEN 'CEO'
	END AS 'Senioridade'
FROM hr.jobs
ORDER BY ROUND((MAX_SALARY + MIN_SALARY) DIV 2, 2), JOB_TITLE;
